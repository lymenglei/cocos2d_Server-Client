package ml.cocos_server;


/**
 * @class stringMaster
 * @author ml
 * @method : decode or encode a string
 */
public class stringMaster {
	public stringMaster(String key){
		this.key = key;
	}
	
	
	public String decode(String s)
	{
		return s;
	}
	public String encode(String s)
	{
		return s;
	}
	
	public String transform(String s)
	{
		return s;
	}
	
	
	private String key;
}
